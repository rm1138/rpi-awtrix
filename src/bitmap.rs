pub struct Bitmap {
    pub bits: Vec<Vec<usize>>,
}

impl Bitmap {
    pub fn from_char(char: char) -> Bitmap {
        let bits: Vec<Vec<usize>> = match char {
            '1' => {
                vec![
                    vec![0, 1, 0, 0, 1],
                    vec![1, 1, 1, 1, 1],
                    vec![0, 0, 0, 0, 1],
                ]
            }
            '2' => {
                vec![
                    vec![1, 0, 1, 1, 1],
                    vec![1, 0, 1, 0, 1],
                    vec![1, 1, 1, 0, 1],
                ]
            }
            '3' => {
                vec![
                    vec![1, 0, 1, 0, 1],
                    vec![1, 0, 1, 0, 1],
                    vec![1, 1, 1, 1, 1],
                ]
            }
            '4' => {
                vec![
                    vec![1, 1, 1, 0, 0],
                    vec![0, 0, 1, 0, 0],
                    vec![1, 1, 1, 1, 1],
                ]
            }
            '5' => {
                vec![
                    vec![1, 1, 1, 0, 1],
                    vec![1, 0, 1, 0, 1],
                    vec![1, 0, 1, 1, 1],
                ]
            }
            '6' => {
                vec![
                    vec![1, 1, 1, 1, 1],
                    vec![1, 0, 1, 0, 1],
                    vec![1, 0, 1, 1, 1],
                ]
            }
            '7' => {
                vec![
                    vec![1, 0, 0, 0, 0],
                    vec![1, 0, 0, 0, 0],
                    vec![1, 1, 1, 1, 1],
                ]
            }
            '8' => {
                vec![
                    vec![1, 1, 1, 1, 1],
                    vec![1, 0, 1, 0, 1],
                    vec![1, 1, 1, 1, 1],
                ]
            }
            '9' => {
                vec![
                    vec![1, 1, 1, 0, 1],
                    vec![1, 0, 1, 0, 1],
                    vec![1, 1, 1, 1, 1],
                ]
            }
            '0' => {
                vec![
                    vec![1, 1, 1, 1, 1],
                    vec![1, 0, 0, 0, 1],
                    vec![1, 1, 1, 1, 1],
                ]
            }
            ':' => {
                vec![vec![0, 1, 0, 1, 0]]
            }
            'C' | 'c' => {
                vec![
                    vec![1, 0, 0, 0, 0],
                    vec![0, 0, 1, 1, 1],
                    vec![0, 0, 1, 0, 1],
                ]
            }
            '%' => {
                vec![
                    vec![1, 0, 0, 1, 1],
                    vec![0, 0, 1, 0, 0],
                    vec![1, 1, 0, 0, 1],
                ]
            }
            'A' => {
                vec![
                    vec![0, 1, 1, 1, 1],
                    vec![1, 0, 1, 0, 0],
                    vec![0, 1, 1, 1, 1],
                ]
            }
            'T' => {
                vec![
                    vec![1, 0, 0, 0, 0],
                    vec![1, 1, 1, 1, 1],
                    vec![1, 0, 0, 0, 0],
                ]
            }
            'M' => {
                vec![
                    vec![1, 1, 1, 1, 1],
                    vec![0, 1, 1, 0, 0],
                    vec![1, 1, 1, 1, 1],
                ]
            }
            'o' => {
                vec![
                    vec![0, 1, 1, 1, 1],
                    vec![0, 1, 0, 0, 1],
                    vec![0, 1, 1, 1, 1],
                ]
            }
            'n' => {
                vec![
                    vec![0, 1, 1, 1, 1],
                    vec![0, 1, 0, 0, 0],
                    vec![0, 1, 1, 1, 1],
                ]
            }

            'u' => {
                vec![
                    vec![0, 1, 1, 1, 1],
                    vec![0, 0, 0, 0, 1],
                    vec![0, 1, 1, 1, 1],
                ]
            }

            'e' => {
                vec![
                    vec![0, 1, 1, 1, 1],
                    vec![0, 1, 0, 0, 1],
                    vec![0, 1, 1, 0, 1],
                ]
            }

            'W' => {
                vec![
                    vec![1, 1, 1, 1, 1],
                    vec![0, 0, 1, 1, 0],
                    vec![1, 1, 1, 1, 1],
                ]
            }

            'd' => {
                vec![
                    vec![0, 1, 1, 1, 1],
                    vec![0, 1, 0, 0, 1],
                    vec![1, 1, 1, 1, 1],
                ]
            }

            'h' => {
                vec![
                    vec![1, 1, 1, 1, 1],
                    vec![0, 1, 0, 0, 0],
                    vec![0, 1, 1, 1, 1],
                ]
            }

            'F' => {
                vec![
                    vec![1, 1, 1, 1, 1],
                    vec![1, 0, 1, 0, 0],
                    vec![1, 0, 0, 0, 0],
                ]
            }
            'r' => {
                vec![
                    vec![0, 1, 1, 1, 1],
                    vec![0, 1, 0, 0, 0],
                    vec![0, 1, 0, 0, 0],
                ]
            }
            'i' => {
                vec![vec![0, 1, 0, 1, 1]]
            }

            'S' => {
                vec![
                    vec![1, 1, 1, 0, 1],
                    vec![1, 0, 1, 0, 1],
                    vec![1, 0, 1, 1, 1],
                ]
            }

            'a' => {
                vec![
                    vec![0, 1, 1, 1, 0],
                    vec![0, 1, 0, 0, 1],
                    vec![0, 1, 1, 1, 1],
                ]
            }

            't' => {
                vec![
                    vec![0, 1, 0, 0, 0],
                    vec![1, 1, 1, 1, 1],
                    vec![0, 1, 0, 0, 1],
                ]
            }

            '-' => {
                vec![vec![0, 0, 1, 0, 0]]
            }
            ' ' => {
                vec![vec![0]]
            }
            '.' => {
                vec![vec![0, 0, 0, 0, 1]]
            }
            _ => {
                vec![]
            }
        };
        Bitmap { bits }
    }
}
